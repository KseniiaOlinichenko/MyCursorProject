package cursor.hw1;

public class HW1ArithmeticOperations {
    public static void main(String[] args) {
        byte b = (-6);
        short s = 2736;
        long l = 283792;
        float f = (-763.87f);
        double d = 827381.44;

        System.out.println("Result of adding: " + (b + f));
        System.out.println("Result of multiplying: " + (s * l));
        System.out.println("Result of subtracting: " + (d - l));
        System.out.println("Result of dividing: " + (b / f));


    }
}
